# Addition
print(10 + 3)
# Substraction
print(10 - 3)
# Division with ans as float
print(10 / 3)
# Division with ans as int
print(10 // 3)
# Remainder
print(10 % 3)
# Power
print(3 ** 3)

# Augmented operator
x = 10

# Addition
x += 3
print(x)
# Substraction
x -= 3
print(x)
# order
# 1. exponentiation 2 ** 2
# 2. division
# 3. multiplication
# 4. addition
# 5. substraction
y = 10 + 3 * 2 ** 2
print(y)
# change order with ()
z = (10 + 3) * 2 ** 2
print(z)

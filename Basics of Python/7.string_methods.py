# def str variable
course = 'Python for Beginners'
# count lenth of str & print
print(len(course))
# covert the entire string to uppercase
print(course.upper())
# covert the entire string to lowercase
print(course.lower())
# covert the entire string to title format (capitalize first letter of every word)
print(course.title())
# find method to find a character or group of them
print(course.find('P')) #find method is case sensitive
# replace method to replace a character or group of them with another
print(course.replace('Beginners', 'Absolute Beginners'))
# in operator returns a boolean operator
print('Python' in course)
